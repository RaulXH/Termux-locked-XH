BLUE="\e[0;50;94m"
AGUA="\e[0;50;36m"
YELOW="\e[1;50;33m"
WHITE="\e[0;50;37m"
FORT="\e[0m"
# Variable de entorno
if [[ -d $PREFIX ]]
then
	if [[ -f $PWD/lockTermux ]]
	then
		sed -i '/lockTermux/d' $PREFIX/etc/profile
		rm -rf $PREFIX/etc/lockTermux 
		cp $PWD/lockTermux $PREFIX/etc
		chmod +x $PREFIX/etc/lockTermux
		FICHERO="$PREFIX/etc/./lockTermux"
		sed -i -e '1i\' -e "$FICHERO" $RUTA
		for i in $(seq 1 $COLS);do echo -n '-';done && echo
		echo -e "\n${AGUA}[*] ${BLUE}Configurado correctamente ${AGUA}[*]\n"
		echo -e "${WHITE}[!] ${BLUE}La contraseña por default es ${YELOW}Termux ${WHITE}[!]${FORT}\n"
		fold -s -w${COLS} <<< $(echo -e "${WHITE}[!] ${BLUE}Para cambiar de contraseña  escribir ${YELOW}Help ${BLUE}o ${YELOW}help, ${BLUE}con esta palabra  entrara  en modo menu e incluso desintalarlo! ${WHITE}[!]")
		echo -e 
		sleep 0.5
		echo -e "${AGUA}[*] ${BLUE}Abre una nueva session ${AGUA}[*]\n${FORT}"
		sleep 1
		for i in $(seq 1 $COLS);do echo -n '-';done && echo
		exit 0
	fi
else
	echo -e "\n${RED}[*] ${BLUE}Este login solo esta para termux ${RED}[*]${FORT}\n"
